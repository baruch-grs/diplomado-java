package org.house;

public class House {
    private final String city = "Mexico City";
    private final String neighborhood = "Huehuetoca";
    private final String color = "Blue";
    private final int bedrooms = 3;
    private final int bathrooms = 2;
    private final int kitchens = 1;

    public String getCity() {
        return city;
    }
    public String getNeighborhood() {
        return neighborhood;
    }
    public String getColor() {
        return color;
    }

    public int getBedrooms() {
        return bedrooms;
    }

    public int getBathrooms() {
        return bathrooms;
    }

    public int getKitchens() {
        return kitchens;
    }
}
